let a = 10;

// Relacionales
console.log(a > 5); // true
console.log(a < 5); // false
console.log(a >= 5); // true
console.log(a <= 5); // false

// Igualdad
console.log(a == 5); // false -- NO USAR
console.log(a === 5); // true -- USAR
console.log(a != 5); // true -- NO USAR
console.log(a !== 5); // false -- USAR